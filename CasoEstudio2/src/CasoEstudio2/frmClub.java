    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
                int id = Integer.parseInt(txtId.getText());
                int numeroCamisa = Integer.parseInt(txtNumCamisa.getText());
                String liga = (String) cmbLP.getSelectedItem();
                String nombre = txtNombre.getText();
                String ubicacion = txtUbicacion.getText();

                // Crear objeto ClubFutbol con los datos del formulario
                ClubFutbol club = new ClubFutbol(numeroCamisa, id, liga, nombre, ubicacion);

                // Crear instancia de Rutina y registrar
                Rutina rutina = new Rutina();
                rutina.registrar(club);

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error en los campos numéricos. Asegúrese de ingresar valores válidos.");
        }
        cargarDatos();
    }//GEN-LAST:event_btnAgregarActionPerformed

