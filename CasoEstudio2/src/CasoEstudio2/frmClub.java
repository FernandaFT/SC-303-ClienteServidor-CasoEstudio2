    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

    // Obtener el id del campo de texto
    int id = Integer.parseInt(txtId.getText());  // Convertir el ID a entero directamente

    // Crear un objeto ClubFutbol
    ClubFutbol club = new ClubFutbol(id, "", "");  // Pasamos valores vacíos si no los necesitamos al crear el objeto

    // Llamar al método buscar
    Rutina rutina = new Rutina();
    rutina.buscar(club);
    txtNombre.setText(club.getNombre());
    txtUbicacion.setText(club.getUbicacion());
    txtNumCamisa.setText(String.valueOf(club.getNumeroCamisa()));
    cmbLP.setSelectedItem(club.getLigaProfesional());
    cargarDatos();
 
    }//GEN-LAST:event_btnBuscarActionPerformed

