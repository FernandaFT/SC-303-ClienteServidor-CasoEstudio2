    public void cargarDatos() {
        DefaultTableModel model = (DefaultTableModel) jtDatos.getModel();
        model.setRowCount(0); // Elimina las filas existentes en la tabla

        Rutina rutina = new Rutina();
        ClubFutbol[] clubes = rutina.obtenerDatos();

        for (ClubFutbol club : clubes) {
            if (club != null) { 
                String[] datos = new String[5];
                datos[0] = String.valueOf(club.getId());
                datos[1] = club.getNombre();
                datos[2] = club.getUbicacion();
                datos[3] = String.valueOf(club.getNumeroCamisa());
                datos[4] = club.getLigaProfesional();
                model.addRow(datos); 
            }
        }

        // Asignar el modelo actualizado a la tabla
        jtDatos.setModel(model);
    }
    
